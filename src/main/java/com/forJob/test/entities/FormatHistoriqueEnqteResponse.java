package com.forJob.test.entities;

public class FormatHistoriqueEnqteResponse {

	private String numEquete;
	
	private String headers;
	
	private String type;

	/**
	 * @return the numEquete
	 */
	public String getNumEquete() {
		return numEquete;
	}

	/**
	 * @param numEquete the numEquete to set
	 */
	public void setNumEquete(String numEquete) {
		this.numEquete = numEquete;
	}

	/**
	 * @return the headers
	 */
	public String getHeaders() {
		return headers;
	}

	/**
	 * @param headers the headers to set
	 */
	public void setHeaders(String headers) {
		this.headers = headers;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	
}
